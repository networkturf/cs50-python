name = "Kunal Vaidya"

#To replace text in strings in a print statement:
print(f"My name is: {name}")
      
#To remove whitespace from a string:
name = name.strip()

#To capitalize first letter: 
name = name.title()

#To split the name into 2 strings: 
first, last = name.split(" ")

print(f"My first name is {first} and last name is {last} ")
